package chapter03;

public class OperationEx8 {
	public static void main(String[] args) {

		int num1 = 10;
		int num2 = 20;

		num1 += num2; // num1 = num1 + num2; 와 같음
		System.out.println(num1);

		num1 -= num2; // num1 = num1 - num2; 와 같음
		System.out.println(num1);

		num1 *= 2; // num1 = num1 * 2; 와 같음
		System.out.println(num1);

		num1 /= 2; // num1 = num1 / 2; 와 같음
		System.out.println(num1);

		num1 %= 2; // num1 = num1 % 2; 와 같음
		System.out.println(num1);

	}

}
